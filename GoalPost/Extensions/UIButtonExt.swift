import UIKit

extension UIButton {
    func setSelectedColor () {
        self.backgroundColor = #colorLiteral(red: 0.6278895548, green: 0.6599422089, blue: 0.8705882353, alpha: 1)
    }
    func setDeselectedColor () {
        self.backgroundColor = #colorLiteral(red: 0.7698370323, green: 0.82716333, blue: 0.8915767766, alpha: 1)
    }
}
